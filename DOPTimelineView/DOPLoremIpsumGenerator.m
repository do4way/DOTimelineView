+ (instancetype)sharedGenerator
{
    static DOPLoremIpsumGenerator *instance;
    if (! instance) {
        instance = [[self alloc] initPrivate];
    }
    return instance;
}

- (NSString *) randomWord
{
    NSInteger randomIndex = arc4random() % [_words count];
    return [_words objectAtIndex:randomIndex];
}

- (NSString*) words:(NSInteger)count
{
    if (count == 0) return @"";
    NSMutableString *words = [[NSMutableString alloc] initWithCapacity:count];
    for (NSInteger i=0; i<count; i++) {
        [words appendString:[self randomWord]];
    }
    return [words copy];
}

- (NSString*) words:(NSInteger)count withSeparator:(NSString *) sep
{
    if (count == 0) return @"";
    NSInteger sepCnt = sep ? count -1 : 0;
    NSMutableString *words = [[NSMutableString alloc] initWithCapacity:(count + sepCnt)];
    for (NSInteger i=0; i<count-1; i++) {
        [words appendString:[self randomWord]];
        if (sep)
            [words appendString:sep];
    }
    [words appendString:[self randomWord]];
    return [words copy];
}

